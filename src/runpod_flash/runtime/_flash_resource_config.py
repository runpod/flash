"""Resource-specific configuration generated at build time.

This module is generated by the Flash build system and contains
resource-specific information about which functions should execute
locally vs remotely.

This file serves as a template and will be overwritten during build.
"""

# Resource name this configuration is for
RESOURCE_NAME: str = ""

# Set of function names that belong to this resource and should execute locally
LOCAL_FUNCTIONS: set[str] = set()

# Set of function names that belong to other resources and need stubs
REMOTE_FUNCTIONS: set[str] = set()


def is_local_function(func_name: str) -> bool:
    """Check if a function should execute locally in this resource.

    Args:
        func_name: Name of the function

    Returns:
        True if function should execute locally, False if it needs a stub
    """
    # If explicitly marked as local, execute locally
    if func_name in LOCAL_FUNCTIONS:
        return True

    # If explicitly marked as remote, create stub
    if func_name in REMOTE_FUNCTIONS:
        return False

    # Default: if not in either set, assume local (safer for backwards compatibility)
    return True
